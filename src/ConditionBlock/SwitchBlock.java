package ConditionBlock;

public class SwitchBlock
{
	public static void main(String []args)
	{
		char day='a';
		switch(day)
		{
			case 'a':
					System.out.println("monday");
					break;
			case 'b':
					System.out.println("Tuesday");
					break;
			case 'c':
					System.out.println("wed");
					break;
		}
		
		String mobail="iphone";
		switch(mobail)
		{
			case"samsung":
					System.out.println("By a samsung phone");
					break;
			case"iphone":
					System.out.println("By a iphone");
					break;
			case"MI":
				System.out.println("Buy a MI phone");
				break;
		}
		
		int Month=3;
		switch(Month)
		{
		case 1:	
			System.out.println("january");
			break;
		case 2:
			System.out.println("February");
			break;
		case 3:
			System.out.println("March");
			break;
		case 4:
			System.out.println("April");
			break;
		case 5:
			System.out.println("May");
			break;
			
		}
		
				
	}
}

	
	

	
	


